# Modify String

a = "python"

print(a.upper())       # Converts all characters to uppercase

print(a.lower())        # Converts all characters to lowercase

b = " Hello Java"
print(b.strip())   # Removes the whitspace from the beginning and the end 

c = "Hello World"
print(c.replace("H","C"))  # Replace string with another string

d = "Hello World"
print(d.split(","))  # splits the string into substrings based on the separator

