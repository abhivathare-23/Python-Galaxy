# shortcut of if-elif chain, while using if-elif statement at the end else block is added which
# is performed if none of the above if-elif statment is true

letter = "A"
if letter == "B":
    print("letter is B")
elif letter == "C":
    print("letter is C")
elif letter == "A":
    print("letter is A")
else:
    print("letter isn't A,B or C")
